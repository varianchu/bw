package com.altostratus.bionicwheels.model;

public class BarGraphModel2 {
	private String productName;
	private String qty;
	private String floorValue;
	private String ceilingValue;

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public String getQty() {
		return qty;
	}

	public void setQty(String qty) {
		this.qty = qty;
	}

	public String getFloorValue() {
		return floorValue;
	}

	public void setFloorValue(String floorValue) {
		this.floorValue = floorValue;
	}

	public String getCeilingValue() {
		return ceilingValue;
	}

	public void setCeilingValue(String ceilingValue) {
		this.ceilingValue = ceilingValue;
	}

}
