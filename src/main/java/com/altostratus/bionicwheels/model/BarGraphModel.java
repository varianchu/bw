package com.altostratus.bionicwheels.model;

public class BarGraphModel {
	private String aboveQty;
	private String belowQty;
	private String qty;
	private String categoryName;

	public String getAboveQty() {
		return aboveQty;
	}

	public void setAboveQty(String aboveQty) {
		this.aboveQty = aboveQty;
	}

	public String getBelowQty() {
		return belowQty;
	}

	public void setBelowQty(String belowQty) {
		this.belowQty = belowQty;
	}

	public String getQty() {
		return qty;
	}

	public void setQty(String qty) {
		this.qty = qty;
	}

	public String getCategoryName() {
		return categoryName;
	}

	public void setCategoryName(String categoryName) {
		this.categoryName = categoryName;
	}

}
